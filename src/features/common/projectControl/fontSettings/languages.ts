export function getLanguageOptions() {
  const displayNames = new Intl.DisplayNames(['zh-TW', 'en'], {
    type: 'language',
  })

  return OPENTYPE_SUPPORTED_LANGUAGES.map((tag) => {
    try {
      const label = displayNames.of(tag)
      return { tag, label: label ? label : tag }
    } catch {
      // Some tag Intl.DisplayNames might not support
      return { tag, label: tag }
    }
  }).sort((a, b) => a.label.localeCompare(b.label, 'zh-TW'))
}
